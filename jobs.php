            <li>Editing and improving website designs based on feedback</li>
            <li>Supporting branding and digital content projects</li>
        </ol>
        <h3>Skills:</h3>
        <ul>
            <li>Creative design thinking (Essential)</li>
            <li>Knowledge of layout, typography, and colour theory (Essential)</li>
